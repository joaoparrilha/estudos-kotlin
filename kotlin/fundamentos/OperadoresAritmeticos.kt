    var idade = 10

    println(idade + 2)
    println(idade - 2)
    println(idade * 2)
    println(idade / 2)
    println(idade % 2)

    // incremento
    // idade++ primeiro usa a variavel e dps incrementa 1 unidade
    // ++idade primeiro incrementa a variavel e dps usa
    idade += 13
    print(idade)

}
package controlefluxo

fun main() {

    val s = "Kotlin"
    var i = 0
    while (i < 100){
        print("$i")
        i++
    }
    var j = 0
    while (j < s.length ){
        println("$j")
        j++
    }

    var d = 100
    while(d > 0){
        println("$d")
        d--
    }
}